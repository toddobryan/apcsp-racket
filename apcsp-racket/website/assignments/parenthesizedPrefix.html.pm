◊title{Parenthesized Prefix Notation}

You're used to writing math expressions with numbers on the outside and
operators on the inside: ◊math{3 + 4}, or ◊math{5 * 2 + 7}.

The problem with this way of writing math is that you have to keep track
of the order of operations (also called operator precedence). That's
PEMDAS or GEMA or whatever way you learned to remember that:
first you
do grouping symbols (like parentheses, brackets, fraction bars, and
the superscript that tells you something is an exponent), then
exponents, then multiplication and division in order left-to-right, then
addition and subtraction in order left-to-right.


